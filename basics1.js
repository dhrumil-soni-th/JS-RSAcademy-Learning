// Declare a variable 'a' with a value of 4 and log its value and type
let a = 4;
console.log(a); // Log the value of 'a'
console.log(typeof a); // Log the type of 'a' (number)

// Declare a variable 'b' with a decimal value and log its type
let b = 234.6;
console.log(typeof b); // Log the type of 'b' (number)

// Declare a variable 'c' with a string value and log its type
var c = "Rahul Shetty";
console.log(typeof c); // Log the type of 'c' (string)

// Declare a boolean variable 'required' and log its type
let required = true;
console.log(typeof required); // Log the type of 'required' (boolean)

// Demonstrate variable reassignment and redeclaration rules
// Note: Redeclaring a variable with 'let' is not allowed, but reassigning is allowed
// let c = a + b (this will not work as 'c' is already declared with 'let')
c = a + b; // Reassigning 'c' with the sum of 'a' and 'b'
console.log(c); // Log the new value of 'c'

// Demonstrate the NOT operator (!) with the boolean variable
console.log(!required); // Log the negation of 'required' (false)

